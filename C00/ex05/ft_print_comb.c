#include <unistd.h>

void	ft_print_comb(void)
{
	int n1;
	int n2;
	int n3;

	n1 = 48;
	while (n1 <= 55)
	{
		n2 = n1 + 1;
		while (n2 <= 56)
		{
			n3 = n2 + 1;
			while (n3 <= 57)
			{
				write(1, &n1, 2);
				write(1, &n2, 2);
				write(1, &n3, 2);
				if (!(n1 == '7' && n2 == '8' && n3 == '9'))
					write(1, ", ", 2);
				n3++;
			}
			n2++;
		}
		n1++;
	}
}
